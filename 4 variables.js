//Variables tipo array
var numeros = [1, 2, 3, 4, 5];

//Imprimir variable tipo array
numeros; //[1, 2, 3, 4, 5]
numeros[0]; // 1
numeros[1]; // 2

/*********************************/

// Variables tipo objeto
var dog = {
    name: "Fido",
    age: 3,
    color: "White"
}

// Imprimir variable tipo objeto
dog; // {name: "Fido", age: 3, color: "White"}
dog.name; // Fido
dog.age; // 3
